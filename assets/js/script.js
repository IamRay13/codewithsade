/**
 * PLAY MUSIC
 */

const playBtn = document.querySelector("[data-play-btn]");

const playMusic = function () {
  if (audioSource.paused) {
    audioSource.play();
    playBtn.classList.add("active");
  } else {
    audioSource.pause();
    playBtn.classList.remove("active");
  }
}

playBtn.addEventListener("click", playMusic);

